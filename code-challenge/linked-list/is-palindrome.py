class Node:
    def __init__(self, value):
        self.value = value
        self.next = None
        self.prev = None
        

class DoublyLinkedList:
    def __init__(self, value):
        new_node = Node(value)
        self.head = new_node
        self.tail = new_node
        self.length = 1

    def print_list(self):
        temp = self.head
        while temp is not None:
            print(temp.value)
            temp = temp.next
        
    def append(self, value):
        new_node = Node(value)
        if self.head is None:
            self.head = new_node
            self.tail = new_node
        else:
            self.tail.next = new_node
            new_node.prev = self.tail
            self.tail = new_node
        self.length += 1
        return True

    def is_palindrome(self):
        if self.length <= 1:
            return True 
        forward = self.head
        backward = self.tail 
        # Traverse first half of list 
        # Use floor division // for odd numbers 
        for _ in range(self.length // 2):
            # Compare first and last value 
            if forward.value != backward.value:
                return False
            # Move node forward one step at time
            forward = forward.next
            # Move node backward one step at time
            backward = backward.prev
            # Continue iterating through list
            # If gone through half of list w/o 
            # non-matching value (from first if statement)
            # Then list is a palindrome 
        return True 




my_dll_1 = DoublyLinkedList(1)
my_dll_1.append(2)
